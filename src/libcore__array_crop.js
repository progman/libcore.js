/**
 * crop array
 * \param[in] source source array
 * \param[in] limit length limit
 * \return result array
 */
function libcore__array_crop(source, limit)
{
	"use strict";

	if ((source instanceof Array) === false)
	{
		return [];
	}

	if (source.length < limit)
	{
		return source;
	}

	return source.slice(0, limit);
}
