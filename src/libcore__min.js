/**
 * find minumal number
 * \param[in] val1 number one
 * \param[in] val1 number two
 * \return minimal number of number one and number two
 */
function libcore__min(val1, val2)
{
	"use strict";

	if (val1 < val2)
	{
		return val1;
	}

	return val2;
}
