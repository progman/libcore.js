/**
 * concat two arrays
 * \param[in] source1 array one
 * \param[in] source2 array two
 * \return result array
 */
function libcore__array_merge(source1, source2)
{
	"use strict";

	if ((source1 instanceof Array) === false)
	{
		return [];
	}

	if ((source2 instanceof Array) === false)
	{
		return [];
	}

	return source1.concat(source2);
}
