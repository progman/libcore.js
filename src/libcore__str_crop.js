/**
 * crop string
 * \param[in] source string
 * \param[in] limit length limit
 * \return croped string
 */
function libcore__str_crop(source, limit)
{
	"use strict";

	if (source.length < limit)
	{
		return source;
	}

	return source.substr(0, limit);
}
